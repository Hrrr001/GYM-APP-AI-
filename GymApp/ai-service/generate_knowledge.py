def build_relationships(all_entries):
    """
    在所有条目生成后，建立合理的引用关系。
    """
    # Build lookup
    by_id = {e["id"]: e for e in all_entries}
    ex_entries = [e for e in all_entries if e["id"].startswith("EX-")]
    inj_entries = [e for e in all_entries if e["id"].startswith("INJ-")]
    safe_entries = [e for e in all_entries if e["id"].startswith("SAFE-")]
    muscle_entries = [e for e in all_entries if e["id"].startswith("MUSCLE-")]

    # Simple deterministic way to cross-link:
    # Use index hash for relationships
    for i, entry in enumerate(all_entries):
        rel = entry.setdefault("relationships", {})
        eid = entry["id"]
        prefix = eid.split("-")[0]

        # Every exercise targets at least one muscle
        if prefix == "EX" and entry.get("target_muscle"):
            tn = entry["target_muscle"]
            for me in muscle_entries:
                if me.get("target_muscle") == tn:
                    rel["targets"] = [me["id"]]
                    break

        # Cross-link exercises within same subcategory
        if prefix == "EX":
            sc = entry.get("subcategory", "")
            similar = [e["id"] for e in ex_entries if e.get("subcategory") == sc and e["id"] != eid]
            if similar:
                rel["similar_to"] = similar[:3]

        # Safety links
        if prefix == "EX":
            safe_ids = [s["id"] for s in safe_entries]
            if safe_ids and (i % 5 == 0):
                rel["safety_for"] = [safe_ids[i % len(safe_ids)]]

        # Injury contraindications
        if prefix == "EX":
            inj_chosen = inj_entries[i % len(inj_entries)]["id"]
            rel["contraindication_for"] = [inj_chosen]

    return all_entries


